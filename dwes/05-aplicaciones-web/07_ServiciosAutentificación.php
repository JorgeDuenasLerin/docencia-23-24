LDAP
====
Extensión/investigación
LDAP = Lightweight Directory Access Protocol
Protocolo para organizar entidades.

En él se apoya Active Directory (Parte del sistema operativo Windows que se
encarga de realizar una administración centralizada)

NOTA: junto con LDAP, Microsoft se apoya en una implementación propia de
Kerberos (Protocolo de autentificación de red) y DNS

Ejemplo de proyecto to en la vida real:
Necesitamos realizar un aplicación intranet de una empresa, los usuarios de
esta intranet serán los registrados en los controladores de dominio. Así los
usuarios solo tendrán un usuario y una contraseña: los de la red Windows.

